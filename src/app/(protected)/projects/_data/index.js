export const projectDetail = (id) => {
  console.log("Searching for project with ID:", id);
  console.log("Available project IDs:", allProjects.data.items.map(item => item.id));
  
  // Try exact match first
  let project = allProjects.data.items.find((item) => item.id === id);
  
  // If not found, try string conversion (in case ID comes as number)
  if (!project) {
    project = allProjects.data.items.find((item) => item.id === String(id));
  }
  
  // If still not found, try number conversion (in case ID comes as string)
  if (!project) {
    project = allProjects.data.items.find((item) => item.id === Number(id));
  }
  
  console.log("Found project:", project);

  return {
    status_code: 200,
    data: project || null,
    version: "1.0.0",
  };
};
